//类名的首字母要大写
class Demo{
	//main函数，函数名采用驼峰式命名
	public static void main(String[] args){
		//字符串常量
		System.out.println("你好!");
		
		//整型常量
		System.out.println(12);
	
		//浮点型常量
		System.out.println(12.12);
		
		//字符型常量
		System.out.println('c');
		
		//boolean型常量
		System.out.println(false);
		
		//null 常量
		//System.out.println(null);
	}
}